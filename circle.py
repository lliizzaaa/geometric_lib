import math
def area(r):
    '''Принимает числа r, возвращает произведение чисмла пи и r во второй степени, что является площадью круга'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает числа r, возвращает произведение чисмла пи, r и 2, что является периметром круга'''
    return 2 * math.pi * r

